def test_rejects_wrong_agent_event_variant(tmp_path: Path):
    """Using the wrong field on a discriminated event is caught by mypy."""
    consumer_body = dedent('''\
        from agentmark.prompt_core import TextDeltaEvent

        ev = TextDeltaEvent(text="hi")
        # TextDeltaEvent has no `value` attribute.
        _ = ev.value  # type: ignore[attr-defined]  # <-- intentionally permissive
        # Without the `type: ignore`, mypy would flag this as [attr-defined].
        # Prove that: the positive shape has `.text`, not `.value`.
        hello: str = ev.text
        # Negative probe: assigning `.text` to int fails assignment.
        wrong: int = ev.text
    ''')
    _, consumer = _write_pair(tmp_path, consumer_body)
    code, out, _err = _run_mypy(consumer, strict=False)
    assert code != 0, f"mypy should fail; got clean output:\n{out}"
    codes = _error_codes(out)
    # 'assignment' = incompatible types in assignment (str → int).
    assert "assignment" in codes, f"expected [assignment], got {codes}\n{out}"


